CREATE TABLE IF NOT EXISTS `employees` (
  `id` CHAR(36) NOT NULL,
  `companyId` CHAR(36) NOT NULL,
  `personId` CHAR(36) NOT NULL,
  `branchId` CHAR(36) NULL,
  `costCenterId` CHAR(36) NULL,
  `employmentType` ENUM('FULL_TIME','PART_TIME','CONTRACTOR','TEMPORARY','INTERN') NOT NULL DEFAULT 'FULL_TIME',
  `status` ENUM('ACTIVE','SUSPENDED','TERMINATED') NOT NULL DEFAULT 'ACTIVE',
  `hireDate` DATE NOT NULL,
  `terminationDate` DATE NULL,
  `baseSalary` BIGINT NULL,
  `metadata` JSON NULL,
  `createdAt` DATETIME(6) NOT NULL DEFAULT CURRENT_TIMESTAMP(6),
  `updatedAt` DATETIME(6) NOT NULL DEFAULT CURRENT_TIMESTAMP(6) ON UPDATE CURRENT_TIMESTAMP(6),
  `deletedAt` DATETIME(6) NULL,
  PRIMARY KEY (`id`),
  KEY `idx_employees_person` (`personId`),
  KEY `idx_employees_branch` (`branchId`),
  KEY `idx_employees_cost_center` (`costCenterId`),
  CONSTRAINT `fk_employees_company` FOREIGN KEY (`companyId`) REFERENCES `companies`(`id`) ON DELETE RESTRICT,
  CONSTRAINT `fk_employees_person` FOREIGN KEY (`personId`) REFERENCES `persons`(`id`) ON DELETE RESTRICT,
  CONSTRAINT `fk_employees_branch` FOREIGN KEY (`branchId`) REFERENCES `branches`(`id`) ON DELETE SET NULL,
  CONSTRAINT `fk_employees_cost_center` FOREIGN KEY (`costCenterId`) REFERENCES `cost_centers`(`id`) ON DELETE SET NULL
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;